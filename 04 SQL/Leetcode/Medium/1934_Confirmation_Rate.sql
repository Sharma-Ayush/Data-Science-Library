--Solution:
Select S.[user_id], ROUND(SUM(Case C.action
							  when 'confirmed' then 1
							  else 0
							  end)/cast(count(*) as float(24)), 2) as confirmation_rate
from dbo.Signups as S
	 left join dbo.Confirmations as C
		on S.[user_id] = C.[user_id]
group by S.[user_id];
